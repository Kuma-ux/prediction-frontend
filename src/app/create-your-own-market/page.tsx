'use client';

import { useState } from 'react';

export default function CreateYourOwnMarketPage() {
  const [title, setTitle] = useState('');
  const [endDate, setEndDate] = useState("");

  const [marketType, setMarketType] =
    useState("standard");

  const [options, setOptions] = useState<string[]>([
    "",
    ""
  ]);

  const [bundlePredictions, setBundlePredictions] =
    useState<string[]>([""]);

  const [description, setDescription] =
    useState('');

  const [resolution, setResolution] =
    useState('');

  const [category, setCategory] =
    useState('');

  function updateOption(index: number, value: string) {
    const copy = [...options];
    copy[index] = value;
    setOptions(copy);
  }

  function addOption() {
    setOptions([...options, ""]);
  }

  function updateBundle(index: number, value: string) {
    const copy = [...bundlePredictions];
    copy[index] = value;
    setBundlePredictions(copy);
  }

  function addBundlePrediction() {
    setBundlePredictions([
        ...bundlePredictions,
        "",
    ])
  }

  async function submitMarket() {
    if(
        !title ||
        !description ||
        !resolution ||
        !category ||
        !endDate
    )
    {
        alert("Please fill all fields");
        return;
    }

    try {

        const res = await fetch(
            "https://api.theprobability.site/markets/submit-market",
            {
                method: "POST",
                credentials: "include",
                headers: {
                    "Content-Type":
                      "application/json",
                },
                body: JSON.stringify({
                    title,
                    description,
                    resolution,
                    category,

                    end_date: endDate,

                    market_type: marketType,

                    options:
                      marketType === "bundle"
                        ? ["YES", "NO"]
                        : options.filter(
                            (o) => o.trim() !== ""
                        ),

                    bundle_predictions:
                      marketType === "bundle"
                        ? bundlePredictions.filter(
                            (p) => p.trim() !== ""
                        )
                        : []
                }),
            }
        );

        const data = await res.json();

        if (!data.success) {
            alert(
                data.error ||
                "Failed to submit market"
            );
            return;
        }

        alert(
            "Market submitted successfully and is awaiting review."
        );

        setTitle("");
        setDescription("");
        setResolution("");
        setCategory("");

        setEndDate("");

        setMarketType("standard");

        setOptions(["", ""]);

        setBundlePredictions([""]);
    } catch (err) {

        console.error(err);

        alert("Failed to submit market");
    }
  }

  return (
    <main className="min-h-screen bg-black text-white">

      <div className="max-w-5xl mx-auto px-6 py-16">

        <h1 className="text-6xl font-black mb-6">
          Create Your Own Market
        </h1>

        <p className="text-zinc-400 text-xl mb-12">
          Turn your knowledge, audience, and
          predictions into a market that
          other people can trade.
        </p>

        {/* SECTION 1 */}

        <div className="space-y-8 mb-16">

          <div>
            <h2 className="text-3xl font-bold mb-4">
              What Is A Prediction Market?
            </h2>

            <p className="text-zinc-300 leading-8">
              A prediction market is a place
              where people buy and sell
              shares based on the likelihood
              of future events occurring.

              If people believe an event is
              likely to happen, they buy
              shares. If they believe it
              won't happen, they buy the
              opposite side.

              Prices move based on supply
              and demand, creating a
              real-time probability estimate
              generated by the crowd.
            </p>
          </div>

          <div>
            <h2 className="text-3xl font-bold mb-4">
              Why Do Prediction Markets Work?
            </h2>

            <p className="text-zinc-300 leading-8">
              Prediction markets aggregate
              information from thousands of
              participants.

              Every buyer and seller is
              putting real money behind what
              they believe.

              This creates powerful economic
              incentives for people to seek
              accurate information and make
              informed decisions.

              Historically, prediction
              markets have often proven more
              accurate than polls, expert
              opinions, and public sentiment
              alone.
            </p>
          </div>

          <div>
            <h2 className="text-3xl font-bold mb-4">
              How Can Market Creators Earn?
            </h2>

            <p className="text-zinc-300 leading-8">
              This feature allows community
              members to create markets that
              attract traders from around
              the platform.

              When a market resolves, the
              creator receives a portion of
              the revenue generated by the
              market.

              The current revenue split is:
            </p>

            <div className="mt-6 bg-zinc-900 border border-white/10 rounded-2xl p-6">

              <div className="text-4xl font-black text-emerald-400">
                35%
              </div>

              <div className="text-zinc-300">
                Market Creator Share
              </div>

              <div className="mt-6 text-4xl font-black">
                65%
              </div>

              <div className="text-zinc-300">
                Platform Share
              </div>
            </div>
          </div>

          <div>
            <h2 className="text-3xl font-bold mb-4">
              Growing Your Market
            </h2>

            <p className="text-zinc-300 leading-8">
              The most successful creators
              actively promote their markets
              on social media platforms,
              communities, newsletters,
              WhatsApp groups, Discord
              servers, Telegram channels,
              podcasts, and other audiences.

              The more traders that
              participate in your market,
              the more activity, liquidity,
              and potential earnings your
              market can generate.
            </p>
          </div>

          <div>
            <h2 className="text-3xl font-bold mb-4">
              Market Integrity
            </h2>

            <p className="text-zinc-300 leading-8">
              Every user-created market is
              reviewed continuously while it
              remains active.

              Markets may be edited,
              suspended, shut down, or
              removed if suspicious
              behaviour, manipulation,
              misleading information,
              fraudulent activity, unclear
              resolution criteria, or other
              irregularities are detected.

              We reserve the right to take
              any action necessary to
              maintain fairness and protect
              participants.
            </p>
          </div>
        </div>

        {/* HOW TO FILL THE FORM */}

        <div
          className="
            mb-12
            bg-zinc-950
            border
            border-white/10
            rounded-3xl
            p-8
          "
        >
          <h2 className="text-3xl font-black mb-6">
            How To Fill The Submission Form
          </h2>

          <p className="text-zinc-400 mb-8">
            Markets with clear wording and clear
            resolution criteria are approved much
            faster. Use the examples below as a guide.
          </p>

          {/* STANDARD MARKET */}
          <div className="mb-10">
            <h3 className="text-xl font-bold mb-4 text-emerald-400">
              Example: Standard Market
            </h3>

            <div className="bg-black rounded-2xl p-6 border border-white/10 space-y-4">
              
              <div>
                <div className="text-zinc-500 text-sm">
                  Title
                </div>
                <div>
                  Will Bitcoin exceed $150,000 before 31 December 2027?
                </div>
              </div>

              <div>
                <div className="text-zinc-500 text-sm">
                  Description
                </div>
                <div>
                  This market tracks whether the price of Bitcoin
                  reaches or exceeds $150,000 USD at any point before
                  31 December 2027.
                </div>
              </div>

              <div>
                <div className="text-zinc-500 text-sm">
                  Resolution Criteria
                </div>
                <div>
                  Resolves YES if Bitcoin trades at or above
                  $150,000 on Binance, Coinbase, or another
                  major exchange before the deadline.
                  Otherwise resolves NO.
                </div>
              </div>

              <div>
                <div className="text-zinc-500 text-sm">
                  Category
                </div>
                <div>Crypto</div>
              </div>

              <div>
                <div className="text-zinc-500 text-sm">
                  Options
                </div>
                <div>YES, NO</div>
              </div>
            </div>
          </div>

          {/* BUNDLE MARKET */}

          <div>
            <h3 className="text-xl font-bold mb-4 text-blue-400">
              Example: Bundle Market
            </h3>

            <div className="bg-black rounded-2xl p-6 border border-white/10 space-y-4">
              
              <div>
                <div className="text-zinc-500 text-sm">
                  Title
                </div>
                <div>
                  Will all of these AI predictions happen before 2030?
                </div>
              </div>

              <div>
                <div className="text-zinc-500 text-sm">
                  Bundle Predictions
                </div>

                <ul className="list-disc pl-5 space-y-1">
                  <li>OpenAI releases GPT-7</li>
                  <li>Humanoid robots become commercially available</li>
                  <li>AI agents handle customer support for major companies</li>
                </ul>
              </div>

              <div>
                <div className="text-zinc-500 text-sm">
                  Resolution Criteria
                </div>
                <div>
                  Resolves YES only if ALL listed predictions
                  occur before 31 December 2030.
                  If any single prediction fails,
                  the market resolves NO.
                </div>
              </div>
            </div>
          </div>
        </div>

        {/* FORM */}

        <div className="
          bg-zinc-950
          border
          border-white/10
          rounded-3xl
          p-8
        ">

          <h2 className="text-3xl font-black mb-8">
            Submit A Market
          </h2>

          <div className="space-y-5">

            <input
              value={title}
              onChange={(e) =>
                setTitle(e.target.value)
              }
              placeholder="Market title"
              className="
                w-full
                bg-zinc-900
                border
                border-white/10
                rounded-xl
                px-4
                py-4
              "
            />

            <textarea
              value={description}
              onChange={(e) =>
                setDescription(e.target.value)
              }
              placeholder="Describe the market"
              rows={6}
              className="
                w-full
                bg-zinc-900
                border
                border-white/10
                rounded-xl
                px-4
                py-4
              "
            />

            <textarea
              value={resolution}
              onChange={(e) =>
                setResolution(e.target.value)
              }
              placeholder="Explain exactly how this market should resolve"
              rows={5}
              className="
                w-full
                bg-zinc-900
                border
                border-white/10
                rounded-xl
                px-4
                py-4
              "
            />

            <input
              value={category}
              onChange={(e) =>
                setCategory(e.target.value)
              }
              placeholder="Category"
              className="
                w-full
                bg-zinc-900
                border
                border-white/10
                rounded-xl
                px-4
                py-4
              "
            />

            <input
              type="datetime-local"
              value={endDate}
              onChange={(e) =>
                setEndDate(e.target.value)
              }
              className="
                w-full
                bg-zinc-900
                border
                border-white/10
                px-4
                py-4
              "
            />

            <select
              value={marketType}
              onChange={(e) =>
                setMarketType(e.target.value)
              }
              className="
                w-full
                bg-zinc-900
                border
                border-white/10
                px-4
                py-4
              "
            >
                <option value="standard">
                    Standard Market
                </option>

                <option value="bundle">
                    Bundle Market
                </option>
            </select>

            {marketType === "standard" && (

                <div className="space-y-3">

                    <h3 className="font-bold">
                        Market Options
                    </h3>

                    {options.map((option, index) => (

                        <input
                          key={index}
                          value={option}
                          onChange={(e) => updateOption(index, e.target.value)}
                          placeholder={`Option ${index + 1}`}
                          className="
                            w-full
                            bg-zinc-900
                            border
                            border-white/10
                            rounded-xl
                            px-4
                            py-4
                          "
                        />
                    ))}

                    <button
                      type="button"
                      onClick={addOption}
                      className="
                        px-4
                        py-2
                        rounded-lg
                        bg-zinc-800
                      "
                    >
                        Option
                    </button>
                </div>
            )}

            {marketType === "bundle" && (

                <div className="space-y-3">

                    <h3 className="font-bold">
                        Bundle Predictions
                    </h3>

                    {bundlePredictions.map(
                        (prediction, index) => (

                            <input
                              key={index}
                              value={prediction}
                              onChange={(e) => updateBundle(index, e.target.value)}
                              placeholder={`Prediction ${index + 1}`}
                              className="
                                w-full
                                bg-zinc-900
                                border
                                border-white/10
                                rounded-xl
                                px-4
                                py-4
                              "
                            />
                        )
                    )}

                    <button
                      type="button"
                      onClick={addBundlePrediction}
                      className="
                        px-4
                        py-2
                        rounded-lg
                        bg-zinc-800
                      "
                    >
                        Add Prediction
                    </button>
                </div>
            )}

            <button
              onClick={submitMarket}
              className="
                bg-emerald-500
                text-black
                font-black
                px-8
                py-4
                rounded-xl
              "
            >
              Submit Market For Review
            </button>

          </div>
        </div>

      </div>

    </main>
  );
}
